const researchDetail = document.getElementById("research-detail");
const researchIndex = new Map(researchItems.map((item) => [item.id, item]));
const pdfjsLib = window.pdfjsLib;

if (pdfjsLib) {
  pdfjsLib.GlobalWorkerOptions.workerSrc =
    "https://cdnjs.cloudflare.com/ajax/libs/pdf.js/3.11.174/pdf.worker.min.js";
}

if (researchDetail) {
  renderResearchDetail();
}

function renderResearchDetail() {
  const params = new URLSearchParams(window.location.search);
  const research = researchIndex.get(params.get("research"));

  if (!research) {
    researchDetail.innerHTML = `
      <section class="detail-empty">
        <p>리서치 정보를 찾을 수 없습니다.</p>
      </section>
    `;
    return;
  }

  researchDetail.innerHTML = "";

  const shell = document.createElement("section");
  shell.className = "detail-shell research-detail-shell";
  shell.innerHTML = `
    <div class="detail-header">
      <p class="eyebrow">UX Research Detail</p>
      <h1>${research.title}</h1>
    </div>
    <div class="detail-grid research-detail-grid">
      <div class="detail-visual-wrap">
        ${
          research.thumbnailPath
            ? `<img class="research-detail-image" src="${toAssetSrc(research.thumbnailPath)}" alt="${research.title} thumbnail" />`
            : `<div class="card-visual research-detail-visual">
                <div class="research-detail-placeholder">
                  <span class="research-chip">Thumbnail</span>
                  <strong>Preview Image</strong>
                  <p>추후 리서치 썸네일 미리보기 이미지가 들어갈 영역</p>
                </div>
              </div>`
        }
      </div>
      <div class="detail-copy research-detail-copy">
        <div class="detail-copy-top">
          <span class="status-badge">${research.category}</span>
        </div>
        <div class="research-overview-list"></div>
      </div>
    </div>
    <section class="detail-media research-chapters-section">
      <div class="detail-media-header">
        <h2 class="detail-media-title">Research Sections</h2>
      </div>
      <div class="research-chapters-grid"></div>
    </section>
  `;

  const overviewList = shell.querySelector(".research-overview-list");
  research.overview.forEach((item) => {
    const block = document.createElement("article");
    block.className = "research-overview-item";
    block.innerHTML = `
      <h3>${item.title}</h3>
      <p>${item.description}</p>
    `;
    overviewList.append(block);
  });

  const chapterGrid = shell.querySelector(".research-chapters-grid");
  research.chapters.forEach((chapter) => {
    const card = document.createElement("article");
    card.className = "research-chapter-card";
    if (chapter.title === "Service Impact") {
      card.classList.add("is-impact");
    }
    if (chapter.title === "UX Strategy & KPI") {
      card.classList.add("is-strategy");
    }
    if (chapter.title === "Learning") {
      card.classList.add("is-divider-anchor");
    }
    card.innerHTML = `
      <h3>${chapter.title}</h3>
      <p>${chapter.description}</p>
    `;
    chapterGrid.append(card);
  });

  const chapterSection = shell.querySelector(".research-chapters-section");
  const pdfSection = createResearchPdfSection(research);
  if (pdfSection) {
    shell.insertBefore(pdfSection, chapterSection);
  }

  researchDetail.append(shell);
  document.title = `${research.title} | Rua's UX Project`;
}

function createResearchPdfSection(research) {
  if (!research.pdfUrl) {
    return null;
  }

  const pdfSource = new URL(research.pdfUrl, window.location.href).href;

  const section = document.createElement("section");
  section.className = "detail-media research-pdf-section";

  const header = document.createElement("div");
  header.className = "detail-media-header";

  const title = document.createElement("h2");
  title.className = "detail-media-title";
  title.textContent = "PDF Preview";

  const headerControls = document.createElement("div");
  headerControls.className = "detail-media-header-controls";

  const prevButton = createSliderButton("prev");
  const nextButton = createSliderButton("next");
  headerControls.append(prevButton, nextButton);
  header.append(title, headerControls);

  const slider = document.createElement("div");
  slider.className = "detail-media-slider";

  const frame = document.createElement("div");
  frame.className = "detail-media-frame";

  const track = document.createElement("div");
  track.className = "detail-media-track";

  const controls = document.createElement("div");
  controls.className = "detail-media-controls";
  const floatingPrevButton = createSliderButton("prev");
  const floatingNextButton = createSliderButton("next");
  controls.append(floatingPrevButton, floatingNextButton);

  const counter = document.createElement("p");
  counter.className = "detail-media-counter";
  counter.textContent = "0 / 0";

  frame.append(track, controls);
  slider.append(frame, counter);
  section.append(header, slider);

  if (!pdfjsLib) {
    const fallback = document.createElement("p");
    fallback.className = "research-pdf-fallback";
    fallback.textContent = "PDF 미리보기를 불러오지 못했습니다.";
    track.append(fallback);
    return section;
  }

  const state = {
    pageCount: 0,
    activeIndex: 0,
    rendered: new Set(),
    canvases: [],
    figures: [],
    pdfDoc: null,
  };

  const allPrevButtons = [prevButton, floatingPrevButton];
  const allNextButtons = [nextButton, floatingNextButton];

  const updateControls = () => {
    counter.textContent = `${state.activeIndex + 1} / ${state.pageCount}`;
    state.figures.forEach((figure, index) => {
      figure.classList.toggle("is-active", index === state.activeIndex);
    });
    const atStart = state.activeIndex === 0;
    const atEnd = state.activeIndex === state.pageCount - 1;
    allPrevButtons.forEach((button) => button.classList.toggle("is-disabled", atStart));
    allNextButtons.forEach((button) => button.classList.toggle("is-disabled", atEnd));
  };

  const renderPage = async (index) => {
    if (state.rendered.has(index) || !state.pdfDoc) {
      return;
    }

    const page = await state.pdfDoc.getPage(index + 1);
    const viewport = page.getViewport({ scale: 1 });
    const targetWidth = Math.min(frame.clientWidth || 1100, 1100);
    const scale = targetWidth / viewport.width;
    const scaledViewport = page.getViewport({ scale });
    const canvas = state.canvases[index];
    const context = canvas.getContext("2d");
    canvas.width = scaledViewport.width;
    canvas.height = scaledViewport.height;
    await page.render({ canvasContext: context, viewport: scaledViewport }).promise;
    state.rendered.add(index);
  };

  const setActiveIndex = async (nextIndex) => {
    if (nextIndex < 0 || nextIndex >= state.pageCount) {
      return;
    }
    state.activeIndex = nextIndex;
    updateControls();
    await renderPage(nextIndex);
  };

  allPrevButtons.forEach((button) => {
    button.addEventListener("click", async () => {
      if (state.activeIndex === 0) {
        return;
      }
      await setActiveIndex(state.activeIndex - 1);
    });
  });

  allNextButtons.forEach((button) => {
    button.addEventListener("click", async () => {
      if (state.activeIndex === state.pageCount - 1) {
        return;
      }
      await setActiveIndex(state.activeIndex + 1);
    });
  });

  pdfjsLib
    .getDocument(pdfSource)
    .promise.then(async (pdfDoc) => {
      state.pdfDoc = pdfDoc;
      state.pageCount = pdfDoc.numPages;

      for (let index = 0; index < state.pageCount; index += 1) {
        const figure = document.createElement("figure");
        figure.className = "detail-media-item research-pdf-item";
        if (index === 0) {
          figure.classList.add("is-active");
        }
        const canvas = document.createElement("canvas");
        canvas.className = "research-pdf-canvas";
        figure.append(canvas);
        track.append(figure);
        state.canvases.push(canvas);
        state.figures.push(figure);
      }

      updateControls();
      await renderPage(0);
    })
    .catch(() => {
      track.innerHTML = "";
      const fallback = document.createElement("p");
      fallback.className = "research-pdf-fallback";
      fallback.textContent = "PDF 미리보기를 불러오지 못했습니다.";
      track.append(fallback);
      counter.textContent = "0 / 0";
    });

  return section;
}

function createSliderButton(direction) {
  const button = document.createElement("button");
  button.type = "button";
  button.className = "detail-media-button";
  button.setAttribute("aria-label", direction === "prev" ? "Previous page" : "Next page");
  button.innerHTML = `
    <svg class="detail-media-arrow" viewBox="0 0 24 24" aria-hidden="true">
      <path d="${direction === "prev" ? "M15 6 9 12 15 18" : "m9 6 6 6-6 6"}"></path>
    </svg>
  `;
  return button;
}

function toAssetSrc(src) {
  if (src.startsWith(".")) {
    return src;
  }

  return `./${src.replace(/^\/+/, "")}`;
}
